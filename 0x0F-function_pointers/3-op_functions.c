#include "3-calc.h"

/**
 * op_add - function that adds 2 int
 * @a: first int
 * @b: second int
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that subs 2 int
 * @a: first int
 * @b: second int
 * Return: sub of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that mul 2 int
 * @a: first int
 * @b: second int
 * Return: mul of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that div 2 int
 * @a: first int
 * @b: second int
 * Return: div of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that gets the mod of 2 int
 * @a: first int
 * @b: second int
 * Return: the remainder of the division of a by b.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
